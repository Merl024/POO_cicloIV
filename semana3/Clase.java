

// Fibonacci sequence

public class Clase{
    public static void main(String[] args){
        for (int n =1; n <= 5; n++ ){
            // indice de donde empieza a donde termina y que accion quiero que realice. 
            System.out.println(n);
        }
        for (int n =1; n <= 5; n+=2  ){
            // Aqui le estoy diciendo que vaya de dos en dos
            System.out.println(n);
        }
    }
}